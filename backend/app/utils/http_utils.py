@lru_cache(maxsize=256)
def _user_agent_for_domain(domain: str) -> str:
    if not domain:
        return random.choice(USER_AGENTS)

    return USER_AGENTS[hash(domain) % len(USER_AGENTS)]


def get_request_headers(url: str | None = None) -> dict[str, str]:
    domain = urlparse(url).netloc if url else ""
    user_agent = _user_agent_for_domain(domain)

    return {
        "User-Agent": user_agent,
        "Accept": (
            "text/html,application/xhtml+xml,application/xml;q=0.9,"
            "image/avif,image/webp,image/apng,*/*;q=0.8"
        ),
        "Accept-Language": "en-US,en;q=0.9",
    }


def parse_datetime(value: Any | None) -> datetime:
    if isinstance(value, datetime):
        dt = value

    elif isinstance(value, (int, float)):
        dt = datetime.fromtimestamp(value, UTC)

    elif isinstance(value, str):
        try:
            dt = dateutil_parse(value, fuzzy=True)
        except Exception:
            return datetime.now(UTC)

    else:
        return datetime.now(UTC)

    if dt.tzinfo is None:
        return dt.replace(tzinfo=UTC)

    return dt.astimezone(UTC)
